package com.lms.enums;

/**
 * Enum representing different content types
 * 
 * @author LMS Team
 * @version 1.0
 */
public enum ContentType {
    /**
     * Video content
     */
    VIDEO,
    
    /**
     * Audio content
     */
    AUDIO,
    
    /**
     * Document content (PDF, DOC, etc.)
     */
    DOCUMENT,
    
    /**
     * Image content
     */
    IMAGE,
    
    /**
     * Text content
     */
    TEXT,
    
    /**
     * Quiz/Assessment content
     */
    QUIZ,
    
    /**
     * Assignment content
     */
    ASSIGNMENT,
    
    /**
     * Interactive content
     */
    INTERACTIVE
}
